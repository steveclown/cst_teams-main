<?php
	class payrollonoutbpjs_model extends CI_Model {
		var $table = "transaction_medical_claim";
		
		public function payrollonoutbpjs_model(){
			parent::__construct();
			$this->CI = get_instance();
		}
		
		public function getCoreDivision(){
			$this->db->select('core_division.division_id, core_division.division_name');
			$this->db->from('core_division');
			$this->db->where('core_division.data_state',0);
			$result = $this->db->get();
			return $result->result_array();
		}

		public function getCoreDepartment(){
			$this->db->select('core_department.department_id, core_department.department_name');
			$this->db->from('core_department');
			$this->db->where('core_department.data_state',0);
			$result = $this->db->get();
			return $result->result_array();
		}

		public function getCoreSection(){
			$this->db->select('core_section.section_id, core_section.section_name');
			$this->db->from('core_section');
			$this->db->where('core_section.data_state',0);
			$result = $this->db->get();
			return $result->result_array();
		}

		public function getHROEmployeeData($employee_id){
			$this->db->select('hro_employee_data.employee_id, hro_employee_data.employee_name, hro_employee_data.division_id, hro_employee_data.department_id, hro_employee_data.section_id');
			$this->db->from('hro_employee_data');
			$this->db->where('hro_employee_data.data_state',0);
			$this->db->where('hro_employee_data.employee_id', $employee_id);
			$result = $this->db->get();
			return $result->row_array();
		}

		public function getHROEmployeeData_Filter($region_id, $branch_id, $location_id, $payroll_employee_level){
			$this->db->select('hro_employee_data.employee_id, hro_employee_data.employee_name');
			$this->db->from('hro_employee_data');
			$this->db->where('hro_employee_data.data_state',0);
			$this->db->where('hro_employee_data.region_id', $region_id);
			$this->db->where('hro_employee_data.branch_id', $branch_id);
			$this->db->where('hro_employee_data.location_id', $location_id);

			if ($payroll_employee_level != 9){
				$this->db->where('hro_employee_data.payroll_employee_level', $payroll_employee_level);
			}

			$result = $this->db->get();
			return $result->result_array();
		}

		public function getHROEmployeeData_OnOutBPJS($region_id, $branch_id, $location_id, $payroll_employee_level, $employee_id, $division_id, $department_id, $section_id){
			$this->db->select('hro_employee_data.employee_id, hro_employee_data.employee_name, hro_employee_data.division_id, hro_employee_data.department_id, hro_employee_data.section_id');
			$this->db->from('hro_employee_data');
			$this->db->where('hro_employee_data.data_state',0);
			$this->db->where('hro_employee_data.region_id', $region_id);
			$this->db->where('hro_employee_data.branch_id', $branch_id);
			$this->db->where('hro_employee_data.location_id', $location_id);

			if ($payroll_employee_level != 9){
				$this->db->where('hro_employee_data.payroll_employee_level', $payroll_employee_level);
			}

			if ($employee_id != ''){
				$this->db->where('hro_employee_data.employee_id', $employee_id);
			}

			if ($division_id != ''){
				$this->db->where('hro_employee_data.division_id', $division_id);
			}

			if ($department_id != ''){
				$this->db->where('hro_employee_data.department_id', $department_id);
			}

			if ($section_id != ''){
				$this->db->where('hro_employee_data.section_id', $section_id);
			}


			$result = $this->db->get();
			return $result->result_array();
		}

		public function getEmployeeName($id){
			$this->db->select('hro_employee_data.employee_name');
			$this->db->from('hro_employee_data');
			$this->db->where('hro_employee_data.employee_id',$id);
			$result = $this->db->get()->row_array();
			if(!isset($result['employee_name'])){
				return '-';
			}else{
				return $result['employee_name'];
			}
		}

		public function getDivisionName($division_id){
			$this->db->select('core_division.division_name');
			$this->db->from('core_division');
			$this->db->where('core_division.division_id', $division_id);
			$this->db->where('data_state',0);
			$result=$this->db->get()->row_array();
			return $result['division_name'];
		}


		public function getDepartmentName($department_id){
			$this->db->select('core_department.department_name');
			$this->db->from('core_department');
			$this->db->where('core_department.department_id', $department_id);
			$this->db->where('data_state',0);
			$result=$this->db->get()->row_array();
			return $result['department_name'];
		}

		public function getSectionName($section_id){
			$this->db->select('core_section.section_name');
			$this->db->from('core_section');
			$this->db->where('core_section.section_id', $section_id);
			$this->db->where('data_state',0);
			$result=$this->db->get()->row_array();
			return $result['section_name'];
		}

		public function getPayrollOnOutBPJS_Data($employee_id){
			$this->db->select('payroll_employee_bpjs.employee_bpjs_id, payroll_employee_bpjs.employee_id, payroll_employee_bpjs.bpjs_in_date, payroll_employee_bpjs.bpjs_reported_salary, payroll_employee_bpjs.bpjs_total_amount, payroll_employee_bpjs.bpjs_kesehatan_status, payroll_employee_bpjs.bpjs_kesehatan_no, payroll_employee_bpjs.bpjs_kesehatan_percentage, payroll_employee_bpjs.bpjs_kesehatan_amount, payroll_employee_bpjs.bpjs_tenagakerja_status, payroll_employee_bpjs.bpjs_tenagakerja_no, payroll_employee_bpjs.bpjs_tenagakerja_percentage, payroll_employee_bpjs.bpjs_tenagakerja_amount, payroll_employee_bpjs.bpjs_remark, payroll_employee_bpjs.bpjs_out_status, payroll_employee_bpjs.bpjs_out_date');
			$this->db->from('payroll_employee_bpjs');
			$this->db->where('payroll_employee_bpjs.data_state',0);
			$this->db->where('payroll_employee_bpjs.employee_id', $employee_id);
			$this->db->order_by('payroll_employee_bpjs.employee_bpjs_id', DESC);
			$result = $this->db->get();
			return $result->result_array();
		}

		public function saveNewPayrollOnOutBPJS($data){
			return $this->db->insert('payroll_employee_bpjs',$data);
		}
		
		public function deletePayrollOnOutBPJS_Data($employee_bpjs_id){
			$this->db->where("payroll_employee_bpjs.employee_bpjs_id", $employee_bpjs_id);
			$query = $this->db->update('payroll_employee_bpjs', array("data_state"=>1));
			if($query){
				return true;
			}else{
				return false;
			}
		}
	}
